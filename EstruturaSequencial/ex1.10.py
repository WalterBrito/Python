'''
Faça um Programa que peça a temperatura em graus Celsius, transforme e
mostre a temperatura em graus Fahrenheit.
'''

print("=======================================================")

c = float(input("Digite o valor da temperatura: "))

f = (c * (9 / 5 ) + 32)
print("Conversao °C para °F e: %.2f" % f)


print("=======================================================")
